        //
        // POST: /Account/ForgotPassword
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> ForgotPassword(ForgotPasswordViewModel model)
        {
            if (ModelState.IsValid)
            {
                ApplicationUser user = db.Users.Where(x => x.Email == model.Email).SingleOrDefault();

                if (user == null)
                {
                    ViewBag.WarningTittle = "Email Tidak Ditemukan";
                    ViewBag.WarningBody = "Email yang anda masukkan tidak ditemukan, mohon periksa kembali atau hubungi administrator untuk mereset password anda.";
                    return View("~/Views/Home/Index.cshtml");
                }

                string password = new Random().Next(100000, 999999).ToString();

                var message = new MailMessage();
                message.To.Add(new MailAddress(model.Email, model.Email)); //replace with valid value
                message.From = new MailAddress("noreply@pibp.id", "Ligita & IBP");
                message.ReplyToList.Add("InternusaIBP@hotmail.com");
                message.Subject = "Reset Password";
                message.Body = string.Format("Dear,<br/><br/>" +
                    "Berikut informasi login anda :<br/>" +
                    "<b>Username : " + user.UserName + "</b><br/>" +
                    "<b>Password : " + password + "</b><br/><br/>" +
                    "Mohon segera ganti password anda setelah berhasil login.");
                message.IsBodyHtml = true;
                using (var smtp = new SmtpClient())
                {
                    try
                    {
                        await smtp.SendMailAsync(message);

                        if (user.PasswordHash != null)
                        {
                            UserManager.RemovePassword(user.Id);
                        }

                        UserManager.AddPassword(user.Id, password);

                        ViewBag.WarningTittle = "Password Berhasil Direset";
                        ViewBag.WarningBody = "Password yang baru sudah dikirim ke email. Mohon segera ganti password anda setelah berhasil login.";
                        return View("~/Views/Home/Index.cshtml");
                    }
                    catch
                    {
                        ViewBag.WarningTittle = "Terjadi Kesalahan";
                        ViewBag.WarningBody = "Terjadi kesalahan saat mengirimkan email. Mohon hubungi administrator anda untuk mereset password.";
                        return View("~/Views/Home/Index.cshtml");
                    }
                }
            }

            ViewBag.WarningTittle = "Masukkan Email Anda";
            ViewBag.WarningBody = "masukkan Email Anda!.";
            return View("~/Views/Home/Index.cshtml");
        }

